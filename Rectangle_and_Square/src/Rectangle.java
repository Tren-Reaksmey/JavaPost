public class Rectangle {
    private double width;
    private double length;

    public Rectangle(){
        this.length=length;
        this.width=width;
    }
    public double getArea(){
        double Size;
        Size=this.length*this.width;
        return Size;
    }
    public double getPerimeter(){
        double Perimeter;
        Perimeter=(length+width)*2;
        return Perimeter;
    }
}
